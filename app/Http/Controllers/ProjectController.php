    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'sum' => 'required',
            'title' => 'required',
            'type_id' => 'required',
            'status_id' => 'required',
            'start' => 'required',
            'end' => 'required',
        ]);

        $project = Project::create([
            'title' => $request->title,
            'type_id' => $request->type_id,
        ]);

        $project->status()->attach($request->status_id,[
            'sum' => $request->sum,
            'start' => $request->start,
            'end' => $request->end,
        ]);

        return redirect('project')->with('status', 'Проект успечно добавлен');

    }

